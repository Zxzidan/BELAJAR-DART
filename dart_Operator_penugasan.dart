void main() {
  // OPerator Penugasan
  num a = 200;

  a += 267.98; // a = a + 267.98
  print(a);

  var i = 5;
  i++; // i = i+1
  print('Untuk Menampilkan variabel i : $i');

  var b = 0;
  var j = b++; // j = b, b++
  var k = ++b; // k = ++b

  print('Ini untuk menampilkan variabel b : $b');
  print('Ini untuk menmapilkan variabel j : $j');
  print('Ini untuk menampilkan variabel k : $k');
}

/*

Sintak Perulangan For

for (init statement; condition; post statement) {
  // kode yang akan diulang
}

1. Init statement akan dieksekusi sekali di awal perulangan.
2. condition akan dilakukan pengecekan dalam setiap perulangan, jika
true perulangan akan dilakukan, jika false perulangan akan dihentikan.
3. Post statement akan dieksekusi setiap kali akhir perulangan.
4. Init statement, condition, dan post statment tidak wajib diisi, jika kondisi
tidak diisi, berarti kondisi selalu bernilai true.

 */

void main() {
  
  // Contoh perulangan tanpa henti
  for (;;) {
    print("Aku Dandi Azaidane");
  
  }
}

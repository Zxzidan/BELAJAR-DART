/// Operator aritmatika
/// + = Pertambahan
/// - , -expression = Pengurangan atau negatif
/// * = perkalian
/// / = Pembagian, Hasil double
/// ~/ = Pembagian, hasil int
/// % = Sisa bagi

void main() {
  int data1 = 200;
  double data2 = 157.5;

  print(data1 + data2);
  print(data1 - data2);
  print(data1 * data2);
  print(data1 / data2);
  print(data1 ~/ data2);
  print(data1 % data2);
}

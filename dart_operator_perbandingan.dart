/// Operator Perbandingan
/// == (sama dengan)
/// != (Tidak sama dengan)
/// > (Lebih dari)
/// < (Kurang dari)
/// >= (Lebih dari atau sama dengan)
/// <= (Kurang dari atau sama dengan)

void main() {
  int data1 = 250;
  double data2 = 270.19;

  print(data1 == data2);
  print(data1 != data2);
  print(data1 > data2);
  print(data1 < data2);
  print(data1 >= data2);
  print(data1 <= data2);
}

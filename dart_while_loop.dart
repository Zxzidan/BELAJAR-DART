/*

1. While loop adalag versi perulangan
yang lebih sederhana dibandingkan for loop.

2. Di while loop, hanya terdapat kondisi perulangan,
tanpa ada init statement dan post statement.

 */

void main (){

var counter = 1;

  while (counter <= 10) {
    print("Ini adalah perulangan ke-$counter");

    counter++;
  }
}
